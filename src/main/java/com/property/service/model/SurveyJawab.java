package com.property.service.model;

public class SurveyJawab {

    private int id;
    private String namaJawaban;
    private Float skor;

    public int getId() {
        return this.id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNamaJawaban() {
        return this.namaJawaban;
    }

    public void setNamaJawaban(String namaJawaban) {
        this.namaJawaban = namaJawaban;
    }

    public Float getSkor() {
        return this.skor;
    }

    public void setSkor(Float skor) {
        this.skor = skor;
    }
    
}
