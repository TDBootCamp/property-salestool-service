package com.property.service.model;

public class Restrukturisasi {

    private int id;
    private String kode;
    private String restrukturisasiKet;

    public int getId() {
        return this.id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getKode() {
        return this.kode;
    }

    public void setKode(String kode) {
        this.kode = kode;
    }

    public String getRestrukturisasiKet() {
        return this.restrukturisasiKet;
    }

    public void setRestrukturisasiKet(String restrukturisasiKet) {
        this.restrukturisasiKet = restrukturisasiKet;
    }
    
}
