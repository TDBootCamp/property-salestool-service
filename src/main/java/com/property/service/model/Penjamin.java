package com.property.service.model;

public class Penjamin {

    private int id;
    private String kodePenjamin;
    private String namaPenjamin;

    public int getId() {
        return this.id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getKodePenjamin() {
        return this.kodePenjamin;
    }

    public void setKodePenjamin(String kodePenjamin) {
        this.kodePenjamin = kodePenjamin;
    }

    public String getNamaPenjamin() {
        return this.namaPenjamin;
    }

    public void setNamaPenjamin(String namaPenjamin) {
        this.namaPenjamin = namaPenjamin;
    }
    
}
