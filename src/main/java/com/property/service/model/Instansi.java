package com.property.service.model;

public class Instansi {

    private int id;
    private String alamatInstansi;
    private String nomorFax;
    private String kodeInstansi;
    private String namaInstansi;
    private String namaKontakPerson;
    private String nomorTelephone;

    public int getId() {
        return this.id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getAlamatInstansi() {
        return this.alamatInstansi;
    }

    public void setAlamatInstansi(String alamatInstansi) {
        this.alamatInstansi = alamatInstansi;
    }

    public String getNomorFax() {
        return this.nomorFax;
    }

    public void setNomorFax(String nomorFax) {
        this.nomorFax = nomorFax;
    }

    public String getKodeInstansi() {
        return this.kodeInstansi;
    }

    public void setKodeInstansi(String kodeInstansi) {
        this.kodeInstansi = kodeInstansi;
    }

    public String getNamaInstansi() {
        return this.namaInstansi;
    }

    public void setNamaInstansi(String namaInstansi) {
        this.namaInstansi = namaInstansi;
    }

    public String getNamaKontakPerson() {
        return this.namaKontakPerson;
    }

    public void setNamaKontakPerson(String namaKontakPerson) {
        this.namaKontakPerson = namaKontakPerson;
    }

    public String getNomorTelephone() {
        return this.nomorTelephone;
    }

    public void setNomorTelephone(String nomorTelephone) {
        this.nomorTelephone = nomorTelephone;
    }
    
}
