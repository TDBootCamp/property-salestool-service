package com.property.service.model;

public class SurveyTanya {

    private int id;
    private Float bobotPertanyaan;
    private String namaPertanyaan;

    public int getId() {
        return this.id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public Float getBobotPertanyaan() {
        return this.bobotPertanyaan;
    }

    public void setBobotPertanyaan(Float bobotPertanyaan) {
        this.bobotPertanyaan = bobotPertanyaan;
    }

    public String getNamaPertanyaan() {
        return this.namaPertanyaan;
    }

    public void setNamaPertanyaan(String namaPertanyaan) {
        this.namaPertanyaan = namaPertanyaan;
    }
    
}
